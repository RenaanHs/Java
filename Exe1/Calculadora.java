public class Calculadora {

    static float soma(float a, float b){
        float result = a + b;
        return result;
    }
    static float sub(float a, float b){
        float result = a - b;
        return result;
    }
    static float mult(float a, float b){
        float result = a * b;
        return result;
    }
    static float div(float a, float b){
        float result = a / b;
        return result;
    }

}
